def _ensure_subjects(session, mandatory: Dict[int, List[Tuple[str, str]]]) -> None:
    """Ensure all mandatory subjects exist. Then ensure all pool subjects."""
    seen = set()
    for items in mandatory.values():
        for code, name in items:
            if code not in seen:
                upsert_subject(session, code, name)
                seen.add(code)
    for _pool_code, rows in ALL_POOLS:
        for code, name in rows:
            if code not in seen:
                upsert_subject(session, code, name)
                seen.add(code)


def _seed_program(
    session,
    program_name: str,
    mandatory: Dict[int, List[Tuple[str, str]]],
    elective_slots: List[Tuple[int, int, List[str]]],
) -> None:
    program = upsert_program(session, program_name)
    program_id = program.id
    clear_program_curriculum(session, program_id)

    _ensure_subjects(session, mandatory)

    for sem, items in mandatory.items():
        for code, _name in items:
            ensure_program_subject(
                session,
                program_id=program_id,
                subject_code=code,
                semester=sem,
                is_mandatory=True,
                elective_group_code=None,
            )

    mandatory_codes = set()
    for items in mandatory.values():
        for code, _ in items:
            mandatory_codes.add(code)

    for sem, num_slots, pool_codes in elective_slots:
        for slot_no in range(1, num_slots + 1):
            ensure_semester_rule_slot(
                session,
                program_id=program_id,
                semester=sem,
                slot_number=slot_no,
                group_codes=pool_codes,
            )

    for group_code, rows in ALL_POOLS:
        for code, _name in rows:
            if code in mandatory_codes:
                continue
            ensure_program_subject(
                session,
                program_id=program_id,
                subject_code=code,
                semester=ELECTIVE_SEMESTER,
                is_mandatory=False,
                elective_group_code=group_code,
            )


def seed_pit(session) -> None:
    """Resync PIT curriculum to authoritative definition."""
    _seed_program(session, PIT_NAME, PIT_MANDATORY, PIT_ELECTIVE_SLOTS)


def seed_siis(session) -> None:
    """Resync SIIS curriculum to authoritative definition."""
    _seed_program(session, SIIS_NAME, SIIS_MANDATORY, SIIS_ELECTIVE_SLOTS)


def seed_imb(session) -> None:
    """Resync IMB curriculum to authoritative definition."""
    _seed_program(session, IMB_NAME, IMB_MANDATORY, IMB_ELECTIVE_SLOTS)


def run_resync_all(session) -> None:
    """Resync all three program curricula."""
    seed_pit(session)
    seed_siis(session)
    seed_imb(session)


if __name__ == "__main__":
    from app.seed.base import run_seed
    run_seed(run_resync_all)
